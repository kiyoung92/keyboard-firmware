void matrix_scan_user(void) {
    if (is_win_lock_held && timer_elapsed32(win_lock_timer) >= 3000) {
        is_win_lock_held = false;

        if (get_highest_layer(default_layer_state) == _WIN) {
            win_lock_active = !win_lock_active;
        }
    }
}

